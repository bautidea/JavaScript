/*  
    'let' is an example of an assignment operator.
*/
let x = 10; // Using let to assign the value 10 to the variable.

// The increment operator (x++;), is equivalent to this. But here we use
//  the assignment operator to add 1 to 'x'.
x = x + 1;
x += 1;
// If the value we want to add is bigger than 1, we can use an assignment
// operator like this.
x += 5;
// For multiplication.
x = x * 3;
x *= 3;

